cities = ["New York", "Los Angeles", "Chicago", "Houston", "Phoenix"]


print(cities[0])  # Output: New York
print(cities[-1]) # Output: Phoenix
print(cities.append("Kismayo"))  # Adds "Kismayo" to the end of the list
print(cities.remove("Chicago"))  # Removes "Chicago" from the list
print(len(cities))  # Output: 5
print(cities)  # Output: ['New York', 'Los Angeles', 'Houston', 'Phoenix', 'Kismayo']